#!/usr/bin/env python3
"""Basic annotations - floor"""
import math


def floor(n: float) -> int:
    """
    Return the floor of a float.

    The floor of a float is the largest
    integer less than or equal to the float.

    Parameters:
    n (float): The float to floor.

    Returns:
    int: The floor of the float.
    """
    return math.floor(n)
